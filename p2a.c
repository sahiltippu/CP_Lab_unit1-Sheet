//C program to display different data types
#include <stdio.h>

int main() {
    int integer = 10;
    float floating = 3.14;
    char character = 'A';
    double doub = 123.456789;
    char string[] = "Hello";

    printf("Integer value: %d\n", integer);
    printf("Float value: %f\n", floating);
    printf("Character value: %c\n", character);
    printf("Double value: %lf\n", doub);
    printf("String value: %s\n", string);

    return 0;
}
